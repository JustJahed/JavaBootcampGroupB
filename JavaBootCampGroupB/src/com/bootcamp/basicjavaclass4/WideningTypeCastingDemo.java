package com.bootcamp.basicjavaclass4;

public class WideningTypeCastingDemo {

	public static void main(String[] args) {
		
		int num1 = 245;
		
		System.out.println("Original Number : "+num1);
		double num1ToDouble  = num1;
		System.out.println("Final Number : "+num1ToDouble);
		
		float num2 = 345.56f;
		double num2Double = num2;
		
		System.out.println("Original Number Float : "+num2);
		System.out.println("Final Number double : "+num2Double);
		
		
		
		
	}
}
