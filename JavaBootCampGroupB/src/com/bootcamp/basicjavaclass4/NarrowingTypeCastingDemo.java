package com.bootcamp.basicjavaclass4;

public class NarrowingTypeCastingDemo {
	
	public static void main(String[] args) {
		
		double num1 = 456.5678;
		int num1ToInt =  (int) num1;
		
		System.out.println("Original Number double : "+num1);
		System.out.println("Final Result int : "+num1ToInt);
		
		
	}

}
