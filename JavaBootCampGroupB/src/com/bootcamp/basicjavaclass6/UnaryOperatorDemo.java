package com.bootcamp.basicjavaclass6;

public class UnaryOperatorDemo {
	
	public static void main(String[] args) {
		
		int num1 = 20;
		num1++;// num1 = num1 + 1
		System.out.println("++ : "+num1);
	
		num1--; // num1 = num1-1
		System.out.println("-- : "+num1);
		
		int num3 = -30;
		num3 = num3*(-1);
		System.out.println(" - :  "+num3);
	}

}
